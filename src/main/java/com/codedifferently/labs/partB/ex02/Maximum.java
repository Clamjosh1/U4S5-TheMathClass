package com.codedifferently.labs.partB.ex02;

public class Maximum {
    public static void main(String[] args) {

        int num1 = 12;
        int num2 = 20;

        int maximum = Math.max(num1, num2);

        System.out.println("Maximum number of " + num1 + " and " + num2 + " is: " + maximum);

    }
}